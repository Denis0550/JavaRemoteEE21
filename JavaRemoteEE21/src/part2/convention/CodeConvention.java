package part2.convention;

// Code convention

// The names have to comply with camel case convention.
// You can read: https://en.wikipedia.org/wiki/Camel_case

// Class names starts with upper-case/capital letter. Next words in the name of classes
// starts with capital letters
public class CodeConvention {

    // Variables' names start with lower-case letter. Next words
    // in variable name starts with upper-case letter
    int variableFirst;

    // Method name starts with lower-case letter. Next words in method name
    // start with upper-case letter
    void doSth() {
    }


    void doSthElse() {
    }

    // ####################################################################################

    // NOTE: names cannot start with a number

    // NOTE: names cannot be reserved Java keyword

    // It is possible to use these symbols in the names: '$' ir '_' but it IS NOT common practise (better don't do this)
    // However  '_'  is used in the names of constants

    // Names have to reflect the purpose of existing of method/class/variable

    // #####################################################################################

    // We use TAB button to move methods, statements, etc to the right

    // When we write code in the block (in the body) we move our code to the right by
    // using TAB button

    // #####################################################################################

    // Names cannot be reserved Java keyword
    // These lines will compile:
//     int public;
//     int class;

}
